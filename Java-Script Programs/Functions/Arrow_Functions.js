/*
    Arrow Functions
*/

// Example 1

const getPrint=()=>{
    console.log("Veer Singh Lodhi");
};

getPrint();
console.log("--------------------------");

// Example 2 

const getSum=(num1,num2)=>{
    return num1+num2;
};

console.log("Sum is "+getSum(10,20));
console.log("-----------------------------");

// Example 3

const getProduct=(num1,num2)=>{
    return num1*num2;
}
console.log("Product is "+getProduct(10,30));

console.log("--------------------------");